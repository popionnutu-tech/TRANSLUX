-- См. план: /Users/ionpop/.claude/plans/vad-ca-este-descrepanta-lazy-piglet.md
--
-- Логика:
--   etolon[i] = km до Chișinău для каждой остановки (упорядоченно по id ASC)
--   km_from_nord[i]     = etolon[i-1] - etolon[i]  (сегмент ОТ предыдущего стопа в TUR направлении)
--   km_from_chisinau[i] = etolon[i] - etolon[i+1]  (сегмент К следующему стопу = ОТ следующего в RETUR)
--   Для первого стопа: km_from_nord=0
--   Для последнего стопа: km_from_chisinau=0
--
-- ВАЖНО: перед применением сделать бэкап
--   CREATE TABLE crm_stop_prices_backup_pre_etolon AS SELECT * FROM crm_stop_prices;

BEGIN;

-- Helper: применить этолон к маршруту по позициям
CREATE OR REPLACE FUNCTION apply_etolon_to_route(p_route_id INT, p_etolons NUMERIC[])
RETURNS VOID AS $$
DECLARE
  v_stop_ids INT[];
  i INT;
  v_count INT;
BEGIN
  SELECT array_agg(id ORDER BY id) INTO v_stop_ids
  FROM crm_stop_prices WHERE crm_route_id = p_route_id;

  v_count := array_length(v_stop_ids, 1);

  IF v_count IS NULL THEN
    RAISE NOTICE 'Route % has no stops', p_route_id;
    RETURN;
  END IF;

  IF v_count != array_length(p_etolons, 1) THEN
    RAISE EXCEPTION 'Route % has % stops but etolon has % values', p_route_id, v_count, array_length(p_etolons, 1);
  END IF;

  FOR i IN 1..v_count LOOP
    UPDATE crm_stop_prices SET
      -- ABS для случаев "детура" (бус идёт назад на короткий участок, route 29)
      km_from_nord = CASE WHEN i = 1 THEN 0 ELSE ABS(p_etolons[i-1] - p_etolons[i]) END,
      km_from_chisinau = CASE WHEN i = v_count THEN 0 ELSE ABS(p_etolons[i] - p_etolons[i+1]) END
    WHERE id = v_stop_ids[i];
  END LOOP;
END $$ LANGUAGE plpgsql;

